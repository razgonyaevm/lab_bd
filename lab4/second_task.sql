SELECT "Н_ЛЮДИ"."ИМЯ", "Н_ОБУЧЕНИЯ"."НЗК", "Н_УЧЕНИКИ"."НАЧАЛО"
FROM "Н_УЧЕНИКИ"
         JOIN "Н_ОБУЧЕНИЯ" ON "Н_УЧЕНИКИ"."ЧЛВК_ИД" = "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД"
         JOIN "Н_ЛЮДИ" ON "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
WHERE "Н_ЛЮДИ"."ФАМИЛИЯ" > 'Афанасьев'
  AND "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" = 105590
  AND "Н_УЧЕНИКИ"."НАЧАЛО" = DATE '1996-09-01';

